package gui;

import java.awt.Color;
import java.awt.Shape;

/**
 * Store shapes when users would like to use.
 * @author tvo93
 * @version November
 */
public class PowerPaintShapes {
    
    /**
     * Set shape.
     */
    private final Shape myShape;
    
    /**
     * Set Color.
     */
    private final Color myColor;
    
    /**
     * Set stroke.
     */
    private final float myStroke;

    /**
     * Constructs for drawShape.
     * 
     * @param theShape the shape
     * @param theColor the color
     * @param theStroke the stroke
     */
    public PowerPaintShapes(final Shape theShape,
                         final Color theColor,
                         final float theStroke) {
        myShape = theShape;
        myColor = theColor;
        myStroke = theStroke;
    }
    
    /**
     * get the shape.
     * @return return the shape
     */
    public Shape getShape() {
        
        return myShape;
    }
    
    /**
     * get the color.
     * @return return the color
     */
    public Color getColor() {
        
        return myColor;
    }
    
    /**
     * get the stroke.  
     * @return return the stroke
     */
    public float getStroke() {
        
        return myStroke;
    }
    
}
